-- lua/marvin/makefile_creator.lua
-- Interactive wizard that generates a Makefile for C, C++, or generic projects.
-- Called from the Jason dashboard "Create" section or :JasonNewMakefile.

local M = {}

local ui = function() return require('marvin.ui') end

-- ── Templates ─────────────────────────────────────────────────────────────────

local function c_template(opts)
  local name  = opts.name
  local cc    = opts.compiler or 'gcc'
  local std   = opts.std or 'c11'
  local src   = opts.src or 'src'
  local inc   = opts.inc or 'include'
  local out   = opts.out or name
  local extra = opts.cflags or ''

  return table.concat({
    '# ' .. name .. ' — generated by Jason/Marvin',
    '',
    'CC      := ' .. cc,
    'CFLAGS  := -std=' .. std .. ' -Wall -Wextra -pedantic' .. (extra ~= '' and (' ' .. extra) or ''),
    'LDFLAGS :=',
    'LIBS    :=',
    '',
    'SRC_DIR := ' .. src,
    'INC_DIR := ' .. inc,
    'OBJ_DIR := build/obj',
    'BIN_DIR := build/bin',
    '',
    'TARGET  := $(BIN_DIR)/' .. out,
    'SRCS    := $(wildcard $(SRC_DIR)/*.c)',
    'OBJS    := $(patsubst $(SRC_DIR)/%.c,$(OBJ_DIR)/%.o,$(SRCS))',
    '',
    '.PHONY: all clean test install dist',
    '',
    'all: $(TARGET)',
    '',
    '$(TARGET): $(OBJS) | $(BIN_DIR)',
    '\t$(CC) $(LDFLAGS) $^ -o $@ $(LIBS)',
    '',
    '$(OBJ_DIR)/%.o: $(SRC_DIR)/%.c | $(OBJ_DIR)',
    '\t$(CC) $(CFLAGS) -I$(INC_DIR) -MMD -MP -c $< -o $@',
    '',
    '$(OBJ_DIR) $(BIN_DIR):',
    '\t@mkdir -p $@',
    '',
    '-include $(OBJS:.o=.d)',
    '',
    'clean:',
    '\t@rm -rf build/',
    '\t@echo "Cleaned."',
    '',
    'test:',
    '\t@echo "No tests configured yet."',
    '',
    'install: $(TARGET)',
    '\t@install -m 755 $(TARGET) /usr/local/bin/' .. out,
    '\t@echo "Installed to /usr/local/bin/' .. out .. '"',
    '',
    'dist: all',
    '\t@mkdir -p dist',
    '\t@cp $(TARGET) dist/',
    '\t@echo "Distribution ready in dist/"',
    '',
  }, '\n')
end

