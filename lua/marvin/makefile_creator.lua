-- ── C template ────────────────────────────────────────────────────────────────
local function c_template(opts)
  local name       = opts.name
  local cc         = opts.compiler or 'gcc'
  local std        = opts.std or 'c11'
  local src        = opts.src or 'src'
  local inc        = opts.inc or 'include'
  local out        = opts.out or name
  local extra      = opts.cflags or ''
  local lflags     = opts.ldflags or ''
  local libs       = opts.libs or ''

  -- Combine auto-detected + user ldflags
  local all_lflags = vim.trim(lflags .. ' ' .. libs)

  return table.concat({
    '# ' .. name .. ' — generated by Marvin',
    '#',
    '# Usage:',
    '#   make          Build the binary',
    '#   make test     Run tests (add test runner)',
    '#   make clean    Remove build artefacts',
    '#   make install  Install to /usr/local/bin',
    '#   make dist     Copy binary to dist/',
    '',
    'CC      := ' .. cc,
    'CFLAGS  := -std=' .. std .. ' -Wall -Wextra -pedantic'
    .. (extra ~= '' and (' ' .. extra) or ''),
    'LDFLAGS := ' .. (all_lflags ~= '' and all_lflags or ''),
    '',
    'SRC_DIR := ' .. src,
    'INC_DIR := ' .. inc,
    'OBJ_DIR := build/obj',
    'BIN_DIR := build/bin',
    '',
    'TARGET  := $(BIN_DIR)/' .. out,
    'SRCS    := $(wildcard $(SRC_DIR)/*.c)',
    'OBJS    := $(patsubst $(SRC_DIR)/%.c,$(OBJ_DIR)/%.o,$(SRCS))',
    'DEPS    := $(OBJS:.o=.d)',
    '',
    '.PHONY: all clean test install dist',
    '',
    'all: $(TARGET)',
    '',
    '$(TARGET): $(OBJS) | $(BIN_DIR)',
    '\t$(CC) $^ -o $@ $(LDFLAGS)',
    '',
    '$(OBJ_DIR)/%.o: $(SRC_DIR)/%.c | $(OBJ_DIR)',
    '\t$(CC) $(CFLAGS) -I$(INC_DIR) -MMD -MP -c $< -o $@',
    '',
    '-include $(DEPS)',
    '',
    '$(OBJ_DIR) $(BIN_DIR):',
    '\t@mkdir -p $@',
    '',
    'clean:',
    '\t@rm -rf build/',
    '\t@echo "Cleaned."',
    '',
    'test:',
    '\t@echo "No test runner configured. Add your test command here."',
    '',
    'install: $(TARGET)',
    '\t@install -m 755 $(TARGET) /usr/local/bin/' .. out,
    '\t@echo "Installed → /usr/local/bin/' .. out .. '"',
    '',
    'dist: all',
    '\t@mkdir -p dist',
    '\t@cp $(TARGET) dist/',
    '\t@echo "Distribution ready in dist/"',
    '',
  }, '\n')
end

-- ── C++ template ──────────────────────────────────────────────────────────────
local function cpp_template(opts)
  local name       = opts.name
  local cxx        = opts.compiler or 'g++'
  local std        = opts.std or 'c++17'
  local src        = opts.src or 'src'
  local inc        = opts.inc or 'include'
  local out        = opts.out or name
  local extra      = opts.cflags or ''
  local lflags     = opts.ldflags or ''
  local libs       = opts.libs or ''

  local all_lflags = vim.trim(lflags .. ' ' .. libs)

  -- Optional sanitizer support
  local san_flags  = ''
  if opts.sanitizer == 'asan' then
    san_flags = ' -fsanitize=address -fno-omit-frame-pointer'
  elseif opts.sanitizer == 'tsan' then
    san_flags = ' -fsanitize=thread'
  elseif opts.sanitizer == 'ubsan' then
    san_flags = ' -fsanitize=undefined'
  end

  -- compile_commands.json generation (via bear or intercept-build)
  local cc_json_section = opts.compile_commands and table.concat({
    '',
    'compile_commands:',
    '\t@if command -v bear >/dev/null 2>&1; then \\',
    '\t  bear -- $(MAKE) all; \\',
    '\telse \\',
    '\t  echo "install bear for compile_commands.json"; \\',
    '\tfi',
  }, '\n') or ''

  return table.concat({
    '# ' .. name .. ' — generated by Marvin',
    '#',
    '# Usage:',
    '#   make                 Build the binary',
    '#   make test            Run tests',
    '#   make clean           Remove build artefacts',
    '#   make install         Install to /usr/local/bin',
    '#   make dist            Copy binary to dist/',
    opts.compile_commands and '#   make compile_commands  Regenerate compile_commands.json (requires bear)' or '',
    '',
    'CXX      := ' .. cxx,
    'CXXFLAGS := -std=' .. std .. ' -Wall -Wextra -pedantic'
    .. (extra ~= '' and (' ' .. extra) or '')
    .. san_flags,
    'LDFLAGS  := ' .. (all_lflags ~= '' and all_lflags or ''),
    '',
    'SRC_DIR  := ' .. src,
    'INC_DIR  := ' .. inc,
    'OBJ_DIR  := build/obj',
    'BIN_DIR  := build/bin',
    '',
    'TARGET   := $(BIN_DIR)/' .. out,
    'SRCS     := $(wildcard $(SRC_DIR)/*.cpp)',
    'OBJS     := $(patsubst $(SRC_DIR)/%.cpp,$(OBJ_DIR)/%.o,$(SRCS))',
    'DEPS     := $(OBJS:.o=.d)',
    '',
    '.PHONY: all clean test install dist' .. (opts.compile_commands and ' compile_commands' or ''),
    '',
    'all: $(TARGET)',
    '',
    '$(TARGET): $(OBJS) | $(BIN_DIR)',
    '\t$(CXX) $^ -o $@ $(LDFLAGS)',
    '',
    '$(OBJ_DIR)/%.o: $(SRC_DIR)/%.cpp | $(OBJ_DIR)',
    '\t$(CXX) $(CXXFLAGS) -I$(INC_DIR) -MMD -MP -c $< -o $@',
    '',
    '-include $(DEPS)',
    '',
    '$(OBJ_DIR) $(BIN_DIR):',
    '\t@mkdir -p $@',
    '',
    'clean:',
    '\t@rm -rf build/',
    '\t@echo "Cleaned."',
    '',
    'test:',
    '\t@echo "No test runner configured. Add your test command here."',
    '',
    'install: $(TARGET)',
    '\t@install -m 755 $(TARGET) /usr/local/bin/' .. out,
    '\t@echo "Installed → /usr/local/bin/' .. out .. '"',
    '',
    'dist: all',
    '\t@mkdir -p dist',
    '\t@cp $(TARGET) dist/',
    '\t@echo "Distribution ready in dist/"',
    cc_json_section,
    '',
  }, '\n')
end

