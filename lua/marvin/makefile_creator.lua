    '# Usage:',
    '#   make          Build binary',
    '#   make test     Run tests',
    '#   make lint     Run linter',
    '#   make fmt      Format source',
    '#   make clean    Remove build artefacts',
    '#   make install  Install binary to GOPATH/bin',
    '',
    'BINARY   := ' .. out,
    'MODULE   := ' .. mod,
    'GOFLAGS  :=',
    'LDFLAGS  := -ldflags="-s -w"',
    '',
    'BUILD_DIR := build',
    '',
    '.PHONY: all build test lint fmt clean install tidy race cover',
    '',
    'all: build',
    '',
    'build:',
